const express  = require("express")
const User = require("../../domain/user.domain")
const {validateParams, validateBody} = require("../../middleware/validateId")
const router = express.Router()
router.patch("/acceptRequest",validateBody,User.acceptRequest)
router.patch("/rejectRequest",validateBody,User.rejectRequest)
router.get("/:userId/posts/",validateParams,User.getPosts)
router.put("/followUser",User.followUser)
router.put("/unfollowUser",User.unfollowUser)
router.put("/:userId",validateParams,User.editDetails)
router.get("/:userId/allPosts",validateParams,User.getAllPostsByFollowing)
router.get("/:userId/followRequests",validateParams,User.getCurrentFollowRequest)
router.get("/:userId/RequestInProgress",validateParams,User.getCurrentRequestInProgress)
router.get("/:userId/followings",validateParams,User.getAllFollowings)
router.get("/:userId/followers",validateParams,User.getAllFollowers)
router.get("/userName/:userName",User.getUserByUserName)
module.exports = router
